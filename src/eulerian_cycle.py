def eulerian_cycle(edges, start=0):
    visited = [[False] * len(ends) for ends in edges]
    cycle_so_far = one_cycle(start, edges, visited)
    i = 0
    while True:
        if i == len(cycle_so_far) - 1:
            break
        current = cycle_so_far[i]
        for end_index, end in enumerate(visited[current]):
            if not end:
                cycle_so_far[i: i + 1] = one_cycle(current, edges, visited)
        i += 1
    return cycle_so_far


def one_cycle(start, edges, visited):
    result = [start]
    current = start
    while current != start or len(result) == 1:
        for end_index, end in enumerate(visited[current]):
            if not end:
                visited[current][end_index] = True
                result.append(edges[current][end_index])
                current = edges[current][end_index]
                break
    return result


def preprocess_edges(edges, pad=False):
    first_step = sorted(((int(start.strip()), list(map(int, (ends.strip().split(','))))) for start, ends in
                        (edge.split('->') for edge in edges.split('\n'))), key=fst)
    if not pad:
        return list(map(snd, first_step))
    result = []
    for start, ends in first_step:
        while start != len(result):
            result.append([])
        result.append(ends)
    return result



