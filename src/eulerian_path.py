def unbalanced_nodes(edges):
    balance = [0] * len(edges)
    for start, ends in enumerate(edges):
        balance[start] += len(ends)
        for end in ends:
            balance[end] -= 1
    try:
        start = next(index for index, element in enumerate(balance) if element > 0)
    except StopIteration:
        start = 0
    try:
        end = next(index for index, element in enumerate(balance) if element < 0)
    except:
        end = 1
    return start, end


def translate_edges(edges):
    occurs = [False] * len(edges)
    for index, ends in enumerate(edges):
        occurs[index] = True
        for end in ends:
            occurs[end] = True

    forward, backward = {}, {}
    last = len(edges) - 1
    for index, occurrence in enumerate(occurs):
        if not occurrence:
            forward[last] = index
            backward[index] = last
            last -= 1
    for index, item in forward.items():
        edges[item] = edges[index]
    edges = edges[:last + 1]

    def mapper(key):
        return forward.get(key, key)

    return [list(map(mapper, edge)) for edge in edges], forward, backward


def eulerian_path(edges):
    new_edges, forward, backward = translate_edges(edges)
    start, end = unbalanced_nodes(new_edges)
    new_edges[end].append(start)
    cycle = eulerian_cycle(new_edges, start)
    cycle.pop()

    def mapper(key):
        return backward.get(key, key)

    return list(map(mapper, cycle))


