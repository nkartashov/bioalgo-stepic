from utils.pyutils import init, tail


def are_adjacent(left, right):
    return len(left) == len(right) and tail(left) == init(right)


class OverlapGraph(object):
    def __init__(self, kmers):
        self._kmers = kmers
        self._adjacency_list = None

    def adjacency_list(self):
        if not self._adjacency_list:
            self._adjacency_list = list(
                (left_kmer, right_kmer) for left_kmer in self._kmers for right_kmer in self._kmers
                if are_adjacent(left_kmer, right_kmer))
        return self._adjacency_list


